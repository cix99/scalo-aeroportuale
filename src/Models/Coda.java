package Models;


public class Coda {

    private String nomeCoda;

    public Coda(String nomeCoda) {
		super();
		this.nomeCoda = nomeCoda;
	}
	
	public Coda() {
	}
	
	public String getNomeCoda () {
		return nomeCoda;
	}

	public void setNomeCoda(String nomeCoda) {
		this.nomeCoda = nomeCoda;
	}    

}
